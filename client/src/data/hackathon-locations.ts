export function getUniqueLocations() {
  const seen = new Set<string>()
  return hackathonEvents.filter((e) => {
    const key = `${e.lat.toFixed(2)},${e.lng.toFixed(2)}`
    if (seen.has(key)) return false
    seen.add(key)
    return true
  })
}

export function getMapDots() {
  const unique = getUniqueLocations()
  return unique.map((event) => ({
    start: { lat: HOME_BASE.lat, lng: HOME_BASE.lng, label: HOME_BASE.label },
    end: { lat: event.lat, lng: event.lng, label: event.school },
  }))
}

